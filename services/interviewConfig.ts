export const getInterviewPhase = (currentRound: number, totalRounds: number): string => {
  if (currentRound === 1) return "opening";
  if (currentRound <= totalRounds * 0.3) return "basic";
  if (currentRound <= totalRounds * 0.7) return "professional";
  if (currentRound <= totalRounds - 2) return "scenario";
  return "closing";
};

export const getPhaseLabel = (phase: string): string => {
  const labels: Record<string, string> = {
    opening: '开场阶段',
    basic: '基础问题',
    professional: '专业深入',
    scenario: '场景题',
    closing: '收尾阶段'
  };
  return labels[phase] || phase;
};

